import UIKit

extension UIColor {

    class var primary: UIColor? { return UIColor(named: "Primary") }
    class var secondary: UIColor? { return UIColor(named: "Secondary") }
    class var tertiary: UIColor? { return UIColor(named: "Tertiary") }
    class var navy: UIColor? { return UIColor(named: "CustomNavy") }
    class var cellHighlight: UIColor? { return UIColor(named: "CellHighlight")}
    class var facebook: UIColor { return UIColor(red: 24/255, green: 119/242, blue: 242/255, alpha: 1) }
    class var kakao: UIColor { return UIColor(red: 254/255, green: 229/242, blue: 0/255, alpha: 1) }
}
